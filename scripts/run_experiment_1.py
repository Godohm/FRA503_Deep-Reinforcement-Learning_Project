"""Experiment 1 — algorithm comparison driver (DDQN + A2C + PPO, MLP-only).

Runs one training job per (algo, seed) combination using the existing
``src.agents.train_dqn / train_a2c / train_ppo`` entrypoints. Each run saves
under deterministic paths so ``--skip-existing`` can re-resume a partial sweep.

Run-id format (deterministic):

    {run_prefix}_{algo}_seed{seed}_steps{steps}

Per run this script ensures the following exist after training:

    models/<algo>/<run_id>/{best.*, final.*, env.yaml, <algo>.yaml, scaler.pkl}
    results/<algo>/<run_id>/{summary.yaml, train_log.csv?, eval_log.csv?,
                             tb/, monitor.csv?, metrics.json, trade_log.csv}

``metrics.json`` and ``trade_log.csv`` are produced by this orchestrator (the
trainers only emit summary.yaml). The trade_log is generated by reloading
``best.{pt,zip}`` and running a deterministic full-pass on the test split.

Usage
-----
    # Plan only:
    python scripts/run_experiment_1.py --dry-run \\
        --algos ddqn a2c ppo --seeds 42 123 2024 --steps 200000 --run-prefix m8_full

    # Run the sweep (long-running, do NOT invoke from an LLM session):
    python scripts/run_experiment_1.py \\
        --algos ddqn a2c ppo --seeds 42 123 2024 --steps 200000 \\
        --run-prefix m8_full --skip-existing
"""
from __future__ import annotations

import argparse
import json
import logging
import sys
import time
import traceback
from pathlib import Path
from typing import Any, Optional

REPO_ROOT = Path(__file__).resolve().parents[1]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

import numpy as np  # noqa: E402
import pandas as pd  # noqa: E402

from src.envs.eurusd_intraday_env import EURUSDIntradayTradingEnv  # noqa: E402
from src.evaluation.backtest import run_backtest  # noqa: E402
from src.evaluation.metrics import compute_all_metrics  # noqa: E402
from src.features.normalization import PriceFeatureScaler  # noqa: E402
from src.features.state_builder import compute_price_features  # noqa: E402
from src.utils.config import load_config  # noqa: E402

ALGO_CHOICES = ("ddqn", "ddqn_expdecay", "a2c", "ppo")

# Map algo key to (trainer-import, config-yaml). `ddqn_expdecay` reuses the
# DDQN trainer but loads dqn_expdecay.yaml (which sets eps_decay_type=exponential).
ALGO_TO_CFG = {
    "ddqn":           "dqn.yaml",
    "ddqn_expdecay":  "dqn_expdecay.yaml",
    "a2c":            "a2c.yaml",
    "ppo":            "ppo.yaml",
}


# --------------------------------------------------------------------------- planning


def _run_id(prefix: str, algo: str, seed: int, steps: int) -> str:
    return f"{prefix}_{algo}_seed{seed}_steps{steps}"


def _algo_save_root(algo: str) -> str:
    """Where to save checkpoints/results. DDQN variants share models/ddqn + results/ddqn."""
    return "ddqn" if algo.startswith("ddqn") else algo


def _planned_runs(algos: list[str], seeds: list[int], steps: int,
                  run_prefix: str) -> list[dict[str, Any]]:
    plan: list[dict[str, Any]] = []
    for algo in algos:
        save_root = _algo_save_root(algo)
        for seed in seeds:
            rid = _run_id(run_prefix, algo, seed, steps)
            plan.append({
                "algo": algo,
                "seed": int(seed),
                "steps": int(steps),
                "run_id": rid,
                "model_dir": Path("models") / save_root / rid,
                "result_dir": Path("results") / save_root / rid,
            })
    return plan


def _print_plan(plan: list[dict[str, Any]]) -> None:
    print(f"[plan] {len(plan)} runs:")
    for r in plan:
        print(f"  - {r['algo']:<5} seed={r['seed']:<5} steps={r['steps']:<7} "
              f"run_id={r['run_id']}")


# --------------------------------------------------------------------------- post-train eval


def _restore_session_day_dtype(df: pd.DataFrame) -> pd.DataFrame:
    if df["session_day"].dtype.name == "string" or df["session_day"].dtype == object:
        df = df.copy()
        df["session_day"] = pd.to_datetime(df["session_day"]).dt.date
    return df


def _build_test_env(env_cfg: dict[str, Any], scaler_path: Path) -> EURUSDIntradayTradingEnv:
    processed_dir = Path(env_cfg["data"]["processed_dir"])
    test_path = processed_dir / "test.parquet"
    if not test_path.is_file():
        raise FileNotFoundError(f"Missing {test_path}; run scripts/prepare_data.py first.")
    test_df = _restore_session_day_dtype(pd.read_parquet(test_path))
    point_size = float(env_cfg["costs"]["point_size"])
    feats = compute_price_features(test_df, point_size=point_size)
    scaler = PriceFeatureScaler.load(scaler_path)
    scaled = scaler.transform(feats)
    return EURUSDIntradayTradingEnv(test_df, scaled, env_cfg, mode="sequential")


def _greedy_policy_for_run(algo: str, model_dir: Path):
    """Reload the saved 'best' model for ``algo`` and return ``policy(obs)->int``."""
    if algo in ("ddqn", "ddqn_expdecay"):
        import torch  # local import
        from src.agents.double_dqn import DDQNConfig, DoubleDQNAgent

        ckpt_path = model_dir / "best.pt"
        ckpt = torch.load(ckpt_path, map_location="cpu", weights_only=False)
        cfg_dict = dict(ckpt["cfg"])
        # torch.save may turn the hidden_sizes tuple into a list — coerce back.
        cfg_dict["hidden_sizes"] = tuple(cfg_dict["hidden_sizes"])
        agent = DoubleDQNAgent(DDQNConfig(**cfg_dict))
        agent.load(ckpt_path)
        def _pi(obs):
            return int(agent.select_action(obs, greedy=True))
        return _pi
    if algo in ("a2c", "ppo"):
        from stable_baselines3 import A2C, PPO
        cls = A2C if algo == "a2c" else PPO
        model = cls.load(str(model_dir / "best.zip"), device="cpu")
        def _pi(obs):
            action, _state = model.predict(obs, deterministic=True)
            return int(np.asarray(action).item())
        return _pi
    raise ValueError(f"Unknown algo: {algo!r}")


def _emit_metrics_and_trade_log(run: dict[str, Any]) -> Optional[dict[str, Any]]:
    """Reload best model, run backtest on test split, save trade_log + metrics."""
    model_dir = run["model_dir"]
    result_dir = run["result_dir"]
    env_yaml = model_dir / "env.yaml"
    scaler_pkl = model_dir / "scaler.pkl"
    if not env_yaml.is_file() or not scaler_pkl.is_file():
        print(f"[warn] {run['run_id']}: missing env.yaml or scaler.pkl, skipping post-eval.")
        return None
    env_cfg = load_config(env_yaml)
    initial_equity = float(env_cfg["portfolio"]["initial_equity"])

    test_env = _build_test_env(env_cfg, scaler_pkl)
    policy = _greedy_policy_for_run(run["algo"], model_dir)
    trade_log = run_backtest(test_env, policy)
    trade_log_path = result_dir / "trade_log.csv"
    result_dir.mkdir(parents=True, exist_ok=True)
    trade_log.to_csv(trade_log_path, index=False)

    metrics = compute_all_metrics(trade_log, initial_equity=initial_equity)
    metrics_dict = metrics.to_dict()
    (result_dir / "metrics.json").write_text(
        json.dumps(metrics_dict, indent=2, default=str), encoding="utf-8"
    )
    return metrics_dict


# --------------------------------------------------------------------------- training dispatch


def _train_one(run: dict[str, Any], config_dir: Path) -> dict[str, Any]:
    """Dispatch to the right trainer based on ``run['algo']``."""
    algo = run["algo"]
    env_cfg = load_config(config_dir / "env.yaml")
    algo_cfg = load_config(config_dir / ALGO_TO_CFG[algo])
    if algo in ("ddqn", "ddqn_expdecay"):
        from src.agents.train_dqn import train_dqn
        return train_dqn(env_cfg, algo_cfg, total_steps=run["steps"],
                         run_id=run["run_id"], seed=run["seed"])
    if algo == "a2c":
        from src.agents.train_a2c import train_a2c
        return train_a2c(env_cfg, algo_cfg, total_steps=run["steps"],
                         run_id=run["run_id"], seed=run["seed"])
    if algo == "ppo":
        from src.agents.train_ppo import train_ppo
        return train_ppo(env_cfg, algo_cfg, total_steps=run["steps"],
                         run_id=run["run_id"], seed=run["seed"])
    raise ValueError(f"Unknown algo: {algo!r}")


# --------------------------------------------------------------------------- main


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Drive Experiment 1: DDQN + A2C + PPO across multiple seeds."
    )
    parser.add_argument("--algos", nargs="+", default=list(ALGO_CHOICES),
                        choices=list(ALGO_CHOICES))
    parser.add_argument("--seeds", nargs="+", type=int, default=[42, 123, 2024])
    parser.add_argument("--steps", type=int, default=200_000)
    parser.add_argument("--config-dir", default="configs")
    parser.add_argument("--run-prefix", default="m8_full")
    parser.add_argument("--skip-existing", action="store_true",
                        help="Skip runs whose results/<algo>/<run_id>/summary.yaml already exists.")
    parser.add_argument("--fail-fast", action="store_true",
                        help="Re-raise the first exception instead of continuing.")
    parser.add_argument("--dry-run", action="store_true",
                        help="Print the planned runs and exit without training.")
    parser.add_argument("--log-level", default="WARNING",
                        help="Logging level for training modules (orchestrator uses print).")
    args = parser.parse_args()

    logging.basicConfig(
        level=getattr(logging, args.log_level.upper()),
        format="%(asctime)s %(levelname)s %(name)s | %(message)s",
    )

    config_dir = Path(args.config_dir)
    plan = _planned_runs(args.algos, args.seeds, args.steps, args.run_prefix)

    if args.dry_run:
        _print_plan(plan)
        print("[dry-run] no training started.")
        return 0

    # Sanity-check that required config files exist before any heavy work.
    for algo in args.algos:
        for fname in ("env.yaml", ALGO_TO_CFG[algo]):
            p = config_dir / fname
            if not p.is_file():
                print(f"[fatal] missing config: {p}")
                return 2

    print(f"[start] {len(plan)} runs, prefix={args.run_prefix}, steps={args.steps}")
    completed: list[dict[str, Any]] = []
    skipped: list[dict[str, Any]] = []
    failed: list[dict[str, Any]] = []
    t_start = time.time()

    for run in plan:
        summary_yaml = run["result_dir"] / "summary.yaml"
        if args.skip_existing and summary_yaml.is_file():
            print(f"[skip] {run['run_id']} (summary.yaml exists)")
            skipped.append(run)
            continue
        t0 = time.time()
        print(f"[run ] {run['run_id']}")
        try:
            summary = _train_one(run, config_dir)
            run["summary"] = summary
            # Post-train eval -> trade_log.csv + metrics.json
            try:
                m = _emit_metrics_and_trade_log(run)
                if m is not None:
                    print(f"       eval: total_return={m['total_return']:.4f} "
                          f"sharpe={m['sharpe_ratio']:.3f} "
                          f"MDD={m['max_drawdown_pct']:.4f} "
                          f"trades={m['n_trades']}")
            except Exception as e:
                print(f"[warn] {run['run_id']}: post-eval failed: {e}")
            completed.append(run)
            print(f"[done] {run['run_id']} ({time.time() - t0:.1f}s)")
        except Exception as e:
            failed.append({**run, "error": str(e), "traceback": traceback.format_exc()})
            print(f"[fail] {run['run_id']}: {type(e).__name__}: {e}")
            if args.fail_fast:
                print("[abort] --fail-fast set; stopping.")
                break

    elapsed = time.time() - t_start
    print(f"[summary] completed={len(completed)} skipped={len(skipped)} "
          f"failed={len(failed)} elapsed={elapsed:.1f}s")
    if failed:
        for r in failed:
            print(f"  - failed: {r['run_id']}: {r['error']}")
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
